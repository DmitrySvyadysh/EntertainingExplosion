namespace EntertainingExplosion.Core.Utils
{
    public static class MathUtils
    {
        public static double GetAverage(double v1, double v2)
        {
            return (v1 + v2) / 2;
        }

        public static int GetAverage(int v1, int v2)
        {
            return (v1 + v2) / 2;
        }
    }
}
